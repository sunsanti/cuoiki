    private void jButton11ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton11ActionPerformed
        // TODO add your handling code here:
        if (!nop4.getText().trim().equals("") || !nop4.getText().trim().equals("Enter Id To Search")){
            try {
                String sql = "SELECT * FROM Employee WHERE [No.] = ?";
                ps = conn.prepareStatement(sql);
                ps.setInt(1, Integer.parseInt(nop4.getText()));
                rs = ps.executeQuery();
                if (rs.next()) {
                    int p = JOptionPane.showConfirmDialog(null, "Are you sure to update this account?", "Update Account", JOptionPane.YES_NO_OPTION);
                    if (p==0) {
                        if (fnp4.getText().trim().equals("") || lnp4.getText().trim().equals("") || anp4.getText().trim().equals("") || spap4.getText().trim().equals("")) {
                            JOptionPane.showMessageDialog(null, "Please fill full form to add employee");
                        }
                        else {
                            try {
                                // TODO add your handling code here:
                                String phonenum = rs.getString("phone");
                                sql = "UPDATE [user] SET "
                                + "pass = '" + String.valueOf(hpap4.getPassword()) + "', "
                                + "fname = '" + fnp4.getText() + "', "
                                + "lname = '" + lnp4.getText() + "', "
                                + "email = '" + ep4.getText() + "', "
                                + "sq = '" + (String)sqp4.getSelectedItem() + "', "
                                + "answer = '" + anp4.getText() + "' "
                                + "WHERE phone = '" +phonenum+ "'";
                                ps = conn.prepareStatement(sql);
                                ps.executeUpdate();
                                if (cp4.getSelectedItem() == "Employee") {
                                    sql = "UPDATE [user] SET class = '0' WHERE phone = '"+phonenum+"'";
                                    ps = conn.prepareStatement(sql);
                                    ps.executeUpdate();
                                }
                                else {
                                    sql = "UPDATE [user] SET class = '-1' WHERE phone = '"+phonenum+"'";
                                    ps = conn.prepareStatement(sql);
                                    ps.executeUpdate();
                                }
                                nop4.setText("                                        Enter Id To Search");
                                nop4.setForeground(new Color(153,153,153));
                                fnp4.setText("");
                                lnp4.setText("");
                                pp4.setText("");
                                ep4.setText("");
                                up4.setText("");
                                anp4.setText("");
                                spap4.setText("");
                                spap4.setVisible(false);
                                hpap4.setText("");
                                hidepanel.setVisible(true);
                                jButton9.setText("Show");
                                sqp4.setSelectedIndex(0);
                                cp4.setSelectedIndex(0);
                                JOptionPane.showMessageDialog(null, "Update Successed");
                            } catch (SQLException ex) {
                                JOptionPane.showMessageDialog(null, ex);
                            }
                        }
                    }
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "Please Enter Id");
            }
        }
    }//GEN-LAST:event_jButton11ActionPerformed

